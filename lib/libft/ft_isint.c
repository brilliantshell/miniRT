#include "libft.h"

int	ft_isint(char *str)
{
	int	k;

	k = -1;
	if (is_charset(str[0], "+-"))
		k++;
	while (str[++k])
		if (!is_digit(str[k], "0123456789"))
			return (0);
	return (1);
}
